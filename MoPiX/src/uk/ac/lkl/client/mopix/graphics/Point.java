package uk.ac.lkl.client.mopix.graphics;

/**
 * @author Ken Kahn
 *
 */
public class Point {

    private int x;
    private int y;

    public Point(int x, int y) {
	this.x = x;
	this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
